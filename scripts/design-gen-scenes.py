SCENES_DIR = PROJECT_ROOT / "assets/scenes/images"
SCENES_DIR.mkdir(parents=True, exist_ok=True)

# 统一风格后缀
STYLE_SUFFIX = """
玄幻短剧风格，半写实画风，电影级质感，
高饱和度色彩，冷暖对比光影，戏剧化打光，
专业场景概念设计，16:9 横构图
"""

def generate_scene(name, prompt, output_file):
    """生成场景图"""
    output_path = SCENES_DIR / output_file

    print(f"生成：{name}")
    print(f"提示词：{prompt[:100]}...")

    payload = {
        "model": "gpt-4o-image",
        "prompt": prompt,
        "n": 1,
        "size": "1792x1024"
    }

    payload_file = Path("/tmp/scene-payload.json")
    with open(payload_file, 'w') as f:
        json.dump(payload, f)

    try:
        result = subprocess.run(
            [str(PROJECT_ROOT / "scripts/api-caller.sh"), "image_gen", "generate", str(payload_file)],
            capture_output=True,
            text=True,
            check=True,
            cwd=PROJECT_ROOT
        )

        response = json.loads(result.stdout)
        url = response['data'][0]['url']

        subprocess.run(
            [str(PROJECT_ROOT / "scripts/api-caller.sh"), "image_gen", "download", url, output_file],
            check=True,
            cwd=SCENES_DIR
        )

        print(f"✓ {output_file}\n")
        return True

    except Exception as e:
        print(f"✗ 生成失败：{e}\n")
        return False

